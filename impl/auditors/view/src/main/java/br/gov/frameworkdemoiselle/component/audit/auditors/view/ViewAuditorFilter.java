package br.gov.frameworkdemoiselle.component.audit.auditors.view;

import java.io.IOException;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import javax.servlet.annotation.WebFilter;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpSession;

@WebFilter(filterName = "demoiselle-audit.auditors.view", value = "/*")
public class ViewAuditorFilter implements Filter {

    private ViewAuditor viewAuditor = null;

    @Override
    public void init(FilterConfig filterConfig) throws ServletException {
        viewAuditor = new ViewAuditor();
    }

    @Override
    public void doFilter(ServletRequest req, ServletResponse response, FilterChain chain) throws IOException, ServletException {

        HttpServletRequest request = (HttpServletRequest) req;

        String sessionId = null;

        HttpSession session = request.getSession(false);
        if (session != null) {
            sessionId = session.getId();
        }

        ViewAuditorInfo info = new ViewAuditorInfo(sessionId, request.getRequestURL().toString(), request.getParameterMap());
        viewAuditor.audit(info);

        chain.doFilter(request, response);

    }

    @Override
    public void destroy() {
    }
}
